# Create two matrices
matrix1 <- matrix(1:9, nrow = 3)
matrix2 <- matrix(9:1, nrow = 3)

# Addition
add_result <- matrix1 + matrix2

# Subtraction
sub_result <- matrix1 - matrix2

# Multiplication
mul_result <- matrix1 * matrix2

# Division
div_result <- matrix1 / matrix2

# Display the results
print("Matrix 1:")
print(matrix1)

print("Matrix 2:")
print(matrix2)

print("Addition:")
print(add_result)

print("Subtraction:")
print(sub_result)

print("Multiplication:")
print(mul_result)

print("Division:")
print(div_result)
