# Create a 4x3 matrix
my_matrix <- matrix(1:12, nrow = 4)

# Display elements of row 1
row1_elements <- my_matrix[1, ]
print(row1_elements)

# Display elements of row 3
row3_elements <- my_matrix[3, ]
print(row3_elements)

# Display elements of column 2
column2_elements <- my_matrix[, 2]
print(column2_elements)
